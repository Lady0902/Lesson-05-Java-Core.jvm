package Amphibia;

public class Frog extends Amphibia {

	public Frog(int weiht, double higth) {
		super(weiht, higth);
		}

	
	public int getWeiht() {
		return super.getWeiht();
	}

	
	public void setWeiht(int weiht) {
		super.setWeiht(weiht);
	}

	
	public double getHigth() {
		return super.getHigth();
	}

	
	public void setHigth(double higth) {
		super.setHigth(higth);
	}

	
	public String toString() {
		return super.toString();
	}


	void eat() {
		super.eat();
		
	}

	
	void sleep() {
		super.sleep();
	}

	void sweem() {
		super.sweem();
	}

	
	void walk() {
		super.walk();
	}

}
